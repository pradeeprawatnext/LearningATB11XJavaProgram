package ex_03_Literals;

public class Lab_033_Literals_P4 {
    public static void main(String[] args) {
        int age =12;// Decima Sytem --base is 10

        //Binary Literal
        int binary_number= 0b1010; //Decima Sytem-10
        //octal base---8
        int octal=0101;
        //heladecimal literals--16
        int hexa=0Xface;
        //base 16 color combinations are in hex-#28a745

        System.out.println(age);
        System.out.println(binary_number);
        System.out.println(octal);
        System.out.println(hexa);

    }
}
