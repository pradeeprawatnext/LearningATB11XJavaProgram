package ex_04_Operators;

public class Lab_038_Unary_Operator {
    public static void main(String[] args) {
        //Unary Operator
        int a= +10;
        int b= -110;
        int result= a+b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(result);

        int c = -1;
        int d= c+1;
        System.out.println(d);
    }
}
